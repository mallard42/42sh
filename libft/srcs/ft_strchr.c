#include "../libft.h"

void	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*b;

	i = 0;
	b = (char*)s;
	while (b[i] != c && b[i] != '\0')
		i++;
	if (b[i] == c)
		return (&b[i]);
	return (NULL);
}
