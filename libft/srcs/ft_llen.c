#include "../libft.h"

int			ft_llen(long int nbr)
{
	int		len;

	len = 1;
	if (nbr < 0)
	{
		nbr = nbr * -1;
		len++;
	}
	while (nbr >= 10)
	{
		len++;
		nbr = nbr / 10;
	}
	return (len);
}
